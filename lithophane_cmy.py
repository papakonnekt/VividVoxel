def load_rgb_image(source: Union[str, Path, Image.Image]) -> Image.Image:
    """Load *source* and return it as an 8-bit RGB :class:`PIL.Image.Image`."""
    if isinstance(source, Image.Image):
        img = source
    else:
        path = Path(source)
        if not path.is_file():
            raise FileNotFoundError(f"Image not found: {path}")
        img = Image.open(path)
        img.load()
    if img.mode != "RGB":
        img = img.convert("RGB")
    return img


def compute_target_pixels(
    src_size: Tuple[int, int],
    width_mm: float,
    height_mm: float,
    px_per_mm: float,
    preserve_aspect: bool = True,
) -> Tuple[int, int]:
    """Return the (width_px, height_px) the image should be resized to."""
    target_w_px, target_h_px = PhysicalSize(width_mm, height_mm).as_pixels(px_per_mm)
    if preserve_aspect:
        src_w, src_h = src_size
        if src_w == 0 or src_h == 0:
            return target_w_px, target_h_px
        scale = min(target_w_px / src_w, target_h_px / src_h)
        target_w_px = max(1, int(round(src_w * scale)))
        target_h_px = max(1, int(round(src_h * scale)))
    return target_w_px, target_h_px


def resize_to_physical(
    img: Image.Image,
    width_mm: float,
    height_mm: float,
    px_per_mm: float,
    preserve_aspect: bool = True,
) -> Image.Image:
    """Resize *img* to the requested physical dimensions at *px_per_mm*."""
    target_w_px, target_h_px = compute_target_pixels(
        img.size, width_mm, height_mm, px_per_mm, preserve_aspect
    )
    if img.size == (target_w_px, target_h_px):
        return img
    return img.resize((target_w_px, target_h_px), resample=_RESAMPLE)


def rgb_to_cmy_thickness(
    img_rgb_float: np.ndarray,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Split an H x W x 3 float image in [0, 1] into CMY thickness channels.

    Subtractive mapping:
        C = 1 - R          (cyan   blocks red   light)
        M = 1 - G          (magenta blocks green light)
        Y = 1 - B          (yellow  absorbs blue  light)
    """
    if img_rgb_float.ndim != 3 or img_rgb_float.shape[2] != 3:
        raise ValueError(
            f"Expected an H x W x 3 array, got shape {img_rgb_float.shape}"
        )
    arr = img_rgb_float.astype(np.float32, copy=False)
    np.clip(arr, 0.0, 1.0, out=arr)
    C = 1.0 - arr[..., 0]
    M = 1.0 - arr[..., 1]
    Y = 1.0 - arr[..., 2]
    return C.astype(np.float32), M.astype(np.float32), Y.astype(np.float32)


def _pad_with_ones(grid: np.ndarray, border_px: int) -> np.ndarray:
    """Pad a grid with border_px rows/cols of 1.0 on every side.

    Accepts both ``(H, W)`` 2-D arrays (single CMY channel / luminance)
    and ``(H, W, C)`` 3-D arrays (RGB image).  Only the first two axes
    are padded; channel axis (if present) is left untouched.
    """
    if border_px <= 0:
        return grid
    pad_width = [(border_px, border_px), (border_px, border_px)]
    if grid.ndim == 3:
        pad_width.append((0, 0))
    return np.pad(
        grid.astype(np.float32, copy=False),
        pad_width,
        mode="constant", constant_values=1.0,
    )


def rgb_to_luminance(img_rgb_float: np.ndarray) -> np.ndarray:
    """Compute BT.601 luminance for an H x W x 3 float image in [0, 1]."""
    if img_rgb_float.ndim != 3 or img_rgb_float.shape[2] != 3:
        raise ValueError(
            f"Expected an H x W x 3 array, got shape {img_rgb_float.shape}"
        )
    arr = img_rgb_float.astype(np.float32, copy=False)
    np.clip(arr, 0.0, 1.0, out=arr)
    luma = arr @ _LUMA_WEIGHTS
    return luma.astype(np.float32)


