def _require_stl() -> None:
    """Raise a friendly error if numpy-stl is not installed."""
    if not _HAS_NUMPY_STL:
        raise RuntimeError(
            "numpy-stl is required for STL output but is not installed.\n"
            "Install it with:  python -m pip install numpy-stl"
        )


def _empty_stl_mesh(n_triangles: int):
    """Return a numpy-stl ``Mesh`` pre-allocated for *n_triangles* triangles."""
    _require_stl()
    return _stl_mesh.Mesh(np.zeros(n_triangles, dtype=_stl_mesh.Mesh.dtype))


def make_base_plate_stl(
    width_mm: float,
    height_mm: float,
    base_thickness_mm: float = DEFAULT_BASE_THICKNESS_MM,
):
    """Build the flat rectangular base plate as a numpy-stl ``Mesh``.

    Occupies ``x âˆˆ [0, width_mm]``, ``y âˆˆ [0, height_mm]``,
    ``z âˆˆ [0, base_thickness_mm]``.  12 triangles (2 per face Ã— 6 faces).
    """
    _require_stl()
    if width_mm <= 0 or height_mm <= 0 or base_thickness_mm <= 0:
        raise ValueError("width_mm, height_mm and base_thickness_mm must be > 0")

    w, h, t = float(width_mm), float(height_mm), float(base_thickness_mm)
    vertices = np.array(
        [
            [0.0, 0.0, 0.0], [w,   0.0, 0.0], [w,   h,   0.0], [0.0, h,   0.0],
            [0.0, 0.0, t],   [w,   0.0, t],   [w,   h,   t],   [0.0, h,   t],
        ],
        dtype=np.float64,
    )
    face_indices = np.array(
        [
            [0, 2, 1], [0, 3, 2],                     # bottom (-Z)
            [4, 5, 6], [4, 6, 7],                     # top    (+Z)
            [0, 1, 5], [0, 5, 4],                     # y = 0  (-Y)
            [1, 2, 6], [1, 6, 5],                     # x = w  (+X)
            [2, 3, 7], [2, 7, 6],                     # y = h  (+Y)
            [3, 0, 4], [3, 4, 7],                     # x = 0  (-X)
        ],
        dtype=np.int32,
    )
    triangles = vertices[face_indices]
    plate = _empty_stl_mesh(triangles.shape[0])
    plate.vectors[:] = triangles.astype(np.float32)
    return plate


# ---- shared column-heightmap mesh helper -----------------------------------

# 12 triangles per pixel, CCW when viewed from outside the column.
_COLUMN_TRI_IDX = np.array(
    [
        [0, 2, 1], [0, 3, 2],                     # bottom (normal -Z)
        [4, 5, 6], [4, 6, 7],                     # top    (normal +Z)
        [0, 1, 5], [0, 5, 4],                     # y = y0 (normal -Y)
        [1, 2, 6], [1, 6, 5],                     # x = x1 (normal +X)
        [2, 3, 7], [2, 7, 6],                     # y = y1 (normal +Y)
        [3, 0, 4], [3, 4, 7],                     # x = x0 (normal -X)
    ],
    dtype=np.int32,
)


def _make_column_heightmap_stl(
    width_mm: float,
    height_mm: float,
    bottom_z: np.ndarray,    # (H, W) bottom-Z of each pixel column
    top_z: np.ndarray,       # (H, W) top-Z of each pixel column
):
    """Build an STL mesh of independent rectangular columns.

    For each pixel ``(i, j)`` a rectangular box is emitted whose bottom sits
    at ``bottom_z[i, j]`` and whose top sits at ``top_z[i, j]``.  The X/Y
    position of pixel ``(i, j)`` is centred on the pixel cell.  12 triangles
    per pixel are emitted (2 bottom, 2 top, 8 side walls).
    """
    _require_stl()
    if bottom_z.shape != top_z.shape or bottom_z.ndim != 2:
        raise ValueError(
            f"bottom_z and top_z must be 2-D arrays of identical shape, "
            f"got {bottom_z.shape} vs {top_z.shape}"
        )
    if width_mm <= 0 or height_mm <= 0:
        raise ValueError("width_mm and height_mm must be > 0")

    H, W = bottom_z.shape
    px_w = float(width_mm) / W
    px_h = float(height_mm) / H

    # Y is flipped so the image is right-side-up when looking at the print
    # from above with +Z up and +Y going away from the viewer.
    cx = (np.arange(W, dtype=np.float64) + 0.5) * px_w
    cy = (H - np.arange(H, dtype=np.float64) - 0.5) * px_h
    cx_grid, cy_grid = np.meshgrid(cx, cy)

    x0 = cx_grid - 0.5 * px_w
    x1 = cx_grid + 0.5 * px_w
    y0 = cy_grid - 0.5 * px_h
    y1 = cy_grid + 0.5 * px_h

    bottom_z = np.asarray(bottom_z, dtype=np.float64)
    top_z = np.asarray(top_z, dtype=np.float64)

    # 8 corner vertices per pixel:  bottom 0..3, top 4..7 (CCW outside).
    verts = np.stack(
        [
            np.stack([x0, y0, bottom_z], axis=-1),  # 0
            np.stack([x1, y0, bottom_z], axis=-1),  # 1
            np.stack([x1, y1, bottom_z], axis=-1),  # 2
            np.stack([x0, y1, bottom_z], axis=-1),  # 3
            np.stack([x0, y0, top_z], axis=-1),     # 4
            np.stack([x1, y0, top_z], axis=-1),     # 5
            np.stack([x1, y1, top_z], axis=-1),     # 6
            np.stack([x0, y1, top_z], axis=-1),     # 7
        ],
        axis=-2,
    )                                                # (H, W, 8, 3)

    triangles = verts[..., _COLUMN_TRI_IDX, :]      # (H, W, 12, 3, 3)
    triangles = triangles.reshape(-1, 3, 3).astype(np.float32)

    mesh_obj = _empty_stl_mesh(triangles.shape[0])
    mesh_obj.vectors[:] = triangles
    return mesh_obj


def make_cmy_layer_stl(
    width_mm: float,
    height_mm: float,
    layer_bottom_z: float,
    channel_intensity: np.ndarray,        # (H, W) float32 in [0, 1]
    max_thickness_mm: float = DEFAULT_MAX_CMY_THICKNESS_MM,
):
    """Build a single CMY color-layer STL with a flat bottom and a
    heightmap top.

    For every pixel ``(i, j)`` the local column extends from the FLAT bottom
    z = ``layer_bottom_z`` to a top z that is a heightmap driven by the
    pixel's intensity:

        z_top[i, j] = layer_bottom_z + channel_intensity[i, j] * max_thickness_mm

    *channel_intensity* is clipped into [0, 1].  The total per-pixel thickness
    is capped at ``max_thickness_mm`` (default **0.5 mm**).
    """
    _require_stl()
    if channel_intensity.ndim != 2:
        raise ValueError(
            f"channel_intensity must be 2-D (H, W), got shape "
            f"{channel_intensity.shape}"
        )
    if max_thickness_mm <= 0:
        raise ValueError("max_thickness_mm must be > 0")

    intensity = np.clip(
        channel_intensity.astype(np.float32, copy=False), 0.0, 1.0
    )
    bottom = np.full_like(intensity, float(layer_bottom_z), dtype=np.float64)
    top = float(layer_bottom_z) + intensity.astype(np.float64) * float(
        max_thickness_mm
    )
    return _make_column_heightmap_stl(width_mm, height_mm, bottom, top)


def make_cmy_layers_stls(
    width_mm: float,
    height_mm: float,
    cyan: np.ndarray,                    # (H, W) float32 in [0, 1]
    magenta: np.ndarray,
    yellow: np.ndarray,
    max_thickness_mm: float = DEFAULT_MAX_CMY_THICKNESS_MM,
    base_bottom_z: float = 0.0,
):
    """Build the three stacked CMY color-layer STLs in a single call.

    The layers are stacked (Cyan at the bottom, then Magenta, then Yellow)
    so that the bottom of each layer is flat at the maximum thickness of the
    layer below it:

        Cyan    bottom z = base_bottom_z
                top    z = base_bottom_z + C * max_thickness_mm
        Magenta bottom z = base_bottom_z + max_thickness_mm
                top    z = base_bottom_z + max_thickness_mm + M * max_thickness_mm
        Yellow  bottom z = base_bottom_z + 2 * max_thickness_mm
                top    z = base_bottom_z + 2 * max_thickness_mm + Y * max_thickness_mm

    Returns a dict ``{"cyan": Mesh, "magenta": Mesh, "yellow": Mesh}``.
    """
    _require_stl()
    if cyan.shape != magenta.shape or magenta.shape != yellow.shape:
        raise ValueError(
            "cyan, magenta and yellow must have the same shape; got "
            f"{cyan.shape}, {magenta.shape}, {yellow.shape}"
        )
    if cyan.ndim != 2:
        raise ValueError("CMY channels must be 2-D (H, W)")

    z_cyan_bottom = float(base_bottom_z)
    z_magenta_bottom = z_cyan_bottom + float(max_thickness_mm)
    z_yellow_bottom = z_magenta_bottom + float(max_thickness_mm)

    return {
        "cyan": make_cmy_layer_stl(
            width_mm, height_mm, z_cyan_bottom, cyan, max_thickness_mm
        ),
        "magenta": make_cmy_layer_stl(
            width_mm, height_mm, z_magenta_bottom, magenta, max_thickness_mm
        ),
        "yellow": make_cmy_layer_stl(
            width_mm, height_mm, z_yellow_bottom, yellow, max_thickness_mm
        ),
    }


def make_lithotop_stl(
    width_mm: float,
    height_mm: float,
    base_thickness_mm: float,
    rgb_normalized: np.ndarray,
    lithotop_min_mm: float = DEFAULT_LITHOTOP_MIN_MM,
    lithotop_max_mm: float = DEFAULT_LITHOTOP_MAX_MM,
    lithotop_bottom_z: Optional[float] = None,
):
    """Build the top white (lithotop) layer as a numpy-stl ``Mesh``.

    Each pixel becomes an independent rectangular column whose height is
    driven by the BT.601 luminance of that pixel (darker = taller).

    By default the bottom of the lithotop sits on top of the base plate,
    i.e. at ``z = base_thickness_mm``.  When stacking CMY color layers
    underneath, pass ``lithotop_bottom_z`` explicitly.
    """
    _require_stl()
    if rgb_normalized.ndim != 3 or rgb_normalized.shape[2] != 3:
        raise ValueError(
            f"rgb_normalized must be H x W x 3, got {rgb_normalized.shape}"
        )
    if lithotop_max_mm < lithotop_min_mm:
        raise ValueError("lithotop_max_mm must be >= lithotop_min_mm")

    luma = rgb_to_luminance(rgb_normalized)                # (H, W) in [0, 1]
    z_extra = float(lithotop_min_mm) + (1.0 - luma) * float(
        lithotop_max_mm - lithotop_min_mm
    )
    bottom_z_default = float(base_thickness_mm)
    z_bottom = (
        float(lithotop_bottom_z)
        if lithotop_bottom_z is not None
        else bottom_z_default
    )
    bottom = np.full_like(luma, z_bottom, dtype=np.float64)
    top = bottom + z_extra.astype(np.float64)
    return _make_column_heightmap_stl(width_mm, height_mm, bottom, top)


def make_frame_stl(
    width_mm: float,
    height_mm: float,
    frame_width_mm: float = DEFAULT_FRAME_WIDTH_MM,
    frame_depth_mm: float = DEFAULT_FRAME_DEPTH_MM,
    bottom_z: float = 0.0,
):
    """Build a hollow rectangular **frame ring** STL.

    The frame is a rectangular ring that wraps the outer perimeter of the
    print.  The outer rectangle is ``width_mm x height_mm``; the inner
    rectangle is ``(width_mm - 2*frame_width_mm) x (height_mm - 2*frame_width_mm)``.
    The ring is solid in Z from ``bottom_z`` to ``bottom_z + frame_depth_mm``.

    Returns ``None`` if ``frame_width_mm <= 0`` or ``frame_depth_mm <= 0``
    or if the inner rectangle would be degenerate.  This is by design so
    that the UI can simply pass ``0`` to disable the frame.

    The frame shares the same X/Y origin as the lithotop and base plate
    (it is anchored at x = 0, y = 0), so it stacks correctly in slicers.
    """
    _require_stl()
    if frame_width_mm <= 0 or frame_depth_mm <= 0:
        return None
    if width_mm <= 0 or height_mm <= 0:
        raise ValueError("width_mm and height_mm must be > 0")

    outer_w = float(width_mm)
    outer_h = float(height_mm)
    inner_w = outer_w - 2.0 * float(frame_width_mm)
    inner_h = outer_h - 2.0 * float(frame_width_mm)
    if inner_w <= 0 or inner_h <= 0:
        return None

    z0 = float(bottom_z)
    z1 = z0 + float(frame_depth_mm)
    x0, x1 = 0.0, outer_w
    y0, y1 = 0.0, outer_h
    ix0, ix1 = float(frame_width_mm), float(frame_width_mm) + inner_w
    iy0, iy1 = float(frame_width_mm), float(frame_width_mm) + inner_h

    # ---- bottom rectangle (outer) ------------------------------------------
    bot_outer = [
        (x0, y0), (x1, y0), (x1, y1), (x0, y1)
    ]
    # ---- bottom rectangle (inner) ------------------------------------------
    bot_inner = [
        (ix0, iy0), (ix1, iy0), (ix1, iy1), (ix0, iy1)
    ]

    triangles = []  # list of (v0, v1, v2)

    def tri(a, b, c):
        triangles.append((a, b, c))

    # Bottom face: outer CCW (-Z) plus inner CW (-Z) so the ring is solid.
    tri((x0, y0, z0), (x1, y0, z0), (x1, y1, z0))
    tri((x0, y0, z0), (x1, y1, z0), (x0, y1, z0))
    tri((ix0, iy0, z0), (ix1, iy1, z0), (ix1, iy0, z0))
    tri((ix0, iy0, z0), (ix0, iy1, z0), (ix1, iy1, z0))

    # Top face: outer CCW (+Z) plus inner CW (+Z).
    tri((x0, y0, z1), (x0, y1, z1), (x1, y1, z1))
    tri((x0, y0, z1), (x1, y1, z1), (x1, y0, z1))
    tri((ix0, iy0, z1), (ix1, iy0, z1), (ix1, iy1, z1))
    tri((ix0, iy0, z1), (ix1, iy1, z1), (ix0, iy1, z1))

    # Outer side walls (4 faces, each split into 2 tris).
    # -Y wall: x in [x0, x1], y = y0
    tri((x0, y0, z0), (x0, y0, z1), (x1, y0, z1))
    tri((x0, y0, z0), (x1, y0, z1), (x1, y0, z0))
    # +X wall: x = x1, y in [y0, y1]
    tri((x1, y0, z0), (x1, y0, z1), (x1, y1, z1))
    tri((x1, y0, z0), (x1, y1, z1), (x1, y1, z0))
    # +Y wall: x in [x1, x0], y = y1
    tri((x1, y1, z0), (x1, y1, z1), (x0, y1, z1))
    tri((x1, y1, z0), (x0, y1, z1), (x0, y1, z0))
    # -X wall: x = x0, y in [y1, y0]
    tri((x0, y1, z0), (x0, y1, z1), (x0, y0, z1))
    tri((x0, y1, z0), (x0, y0, z1), (x0, y0, z0))

    # Inner side walls (4 faces) -- flipped winding so normals point inward.
    # -Y inner wall: y = iy0
    tri((ix0, iy0, z0), (ix1, iy0, z1), (ix0, iy0, z1))
    tri((ix0, iy0, z0), (ix1, iy0, z0), (ix1, iy0, z1))
    # +X inner wall: x = ix1
    tri((ix1, iy0, z0), (ix1, iy0, z1), (ix1, iy1, z1))
    tri((ix1, iy0, z0), (ix1, iy1, z1), (ix1, iy1, z0))
    # +Y inner wall: y = iy1
    tri((ix1, iy1, z0), (ix0, iy1, z1), (ix1, iy1, z1))
    tri((ix1, iy1, z0), (ix0, iy1, z0), (ix0, iy1, z1))
    # -X inner wall: x = ix0
    tri((ix0, iy1, z0), (ix0, iy1, z1), (ix0, iy0, z1))
    tri((ix0, iy1, z0), (ix0, iy0, z1), (ix0, iy0, z0))

    tri_arr = np.array(triangles, dtype=np.float32)         # (N, 3, 3)
    mesh_obj = _empty_stl_mesh(tri_arr.shape[0])
    mesh_obj.vectors[:] = tri_arr
    return mesh_obj


def make_depth_preview(
    source: Union[str, Path, Image.Image],
    width_mm: float,
    height_mm: float,
    lithotop_min_mm: float,
    lithotop_max_mm: float,
    border_width_mm: float = DEFAULT_BORDER_WIDTH_MM,
    px_per_mm: float = 4.0,
) -> Image.Image:
    """Render an instant 2.5D grayscale depth-map preview.

    The preview maps the image's BT.601 luminance onto a grayscale
    heightmap: white pixels (high luminance) become the thinnest
    columns; black pixels (low luminance) become the thickest columns.
    Border pixels are clamped to ``min`` so the border always prints
    thin.  The returned image is a PIL ``L`` mode image of size
    ``(width_mm * px_per_mm, height_mm * px_per_mm)``.

    This is a lightweight Pillow-only path designed for the live
    Streamlit preview; the heavy STL pipeline is run separately.
    """
    img = load_rgb_image(source)
    target_w = max(1, int(round(width_mm * px_per_mm)))
    target_h = max(1, int(round(height_mm * px_per_mm)))
    if img.size != (target_w, target_h):
        img = img.resize((target_w, target_h), resample=_RESAMPLE)

    # BT.601 luminance
    arr = np.asarray(img, dtype=np.float32) / 255.0      # H x W x 3
    luma = arr @ _LUMA_WEIGHTS                            # H x W

    # Pad with border (1.0 = thin = white in the preview)
    border_px = max(0, int(round(border_width_mm * px_per_mm)))
    if border_px > 0:
        luma = np.pad(
            luma,
            ((border_px, border_px), (border_px, border_px)),
            mode="constant", constant_values=1.0,
        )

    # Map [0,1] luminance -> [min,max] thickness, then invert for grayscale:
    # darker pixel = thicker column = darker in the preview.
    t_min = float(lithotop_min_mm)
    t_max = float(lithotop_max_mm)
    if t_max <= t_min:
        t_max = t_min + 0.1
    thickness = t_min + (1.0 - luma) * (t_max - t_min)
    thickness = np.clip(thickness, t_min, t_max)
    # Normalize to [0, 1] for display: max thickness -> 0 (black).
    gray = 1.0 - (thickness - t_min) / (t_max - t_min)
    gray_u8 = np.clip(gray * 255.0, 0, 255).astype(np.uint8)
    return Image.fromarray(gray_u8, mode="L")


