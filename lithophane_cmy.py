"""
lithophane_cmy.py
=================

Convert an uploaded RGB image into everything needed to 3D-print a CMY
lithophane:

* Cyan / Magenta / Yellow **thickness channels** for a CMY lithophane
* A flat **base-plate STL** (default 0.6 mm thick)
* Three **CMY color-layer STLs** -- one per ink -- with a flat bottom and a
  heightmap top, each capped at 0.5 mm of thickness.
* A **lithotop STL** whose per-pixel height is driven by the luminance of
  the original image (shadows and highlights).

Print layout (Z grows upward, all dimensions in mm):

    z=2.0+    lithotop top  (varies with luminance)
    z=1.0+    lithotop bottom = base + max CMY stack height = 0.6 + 1.5
              â”Œâ”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ Yellow layer  top = 1.0 + Y*0.5
              â”‚ bottom = 1.0 (flat)
              â”œâ”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ Magenta layer top = 0.5 + M*0.5
              â”‚ bottom = 0.5 (flat)
              â”œâ”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ Cyan layer    top = 0.0 + C*0.5
              â”‚ bottom = 0.0 (flat)
    z=0.0      Base plate (flat, 0.6 mm thick)

CMY channel mapping (subtractive colour model):

    C  = 1 - R_norm        # cyan   absorbs red   light
    M  = 1 - G_norm        # magenta absorbs green light
    Y  = 1 - B_norm        # yellow  absorbs blue  light

    1.0 -> the pixel should be printed at MAX thickness (it absorbs /
           blocks the most light of that colour)
    0.0 -> the pixel should be printed at MIN thickness (lets the colour
           through)

Lithotop luminance (BT.601):

    L  = 0.299 R + 0.587 G + 0.114 B
    z_top = base + 3*max_cmy + z_min + (1 - L) * (z_max - z_min)

Pipeline
--------
1.  Load RGB image (any mode -> converted to RGB).
2.  Resize to a target physical size (width_mm, height_mm) at a chosen
    resolution (px_per_mm).  Aspect ratio is preserved by default.
3.  Normalise to float32 in [0, 1].
4.  Split into Cyan, Magenta, Yellow thickness channels.
5.  Compute BT.601 luminance for the lithotop layer.
6.  Build the base plate, three CMY color layers and the lithotop meshes.

Alignment guarantee
-------------------
All five STL meshes share **bit-exact X and Y coordinates**:

* every mesh is generated by ``_make_column_heightmap_stl`` (or
  ``make_base_plate_stl``) using the same ``width_mm``, ``height_mm`` and
  pixel grid, so each pixel ``(i, j)`` sits at exactly the same ``(x, y)``
  position in every STL;
* all X coordinates lie in ``[0, width_mm]``, all Y coordinates lie in
  ``[0, height_mm]``, and every mesh's bounding box is identical to within
  floating-point precision.

Call :func:`verify_xy_alignment` (or ``result.verify_alignment()``) to
assert this in your own code; the CLI runs the same check by default when
STLs are emitted.

Slicer-friendly file naming
----------------------------
By default :func:`save_stls` writes five numbered STLs in the order a
multi-material slicer (Bambu Studio, PrusaSlicer, OrcaSlicer, ...) should
load them:

    1_Base_White.stl     (the structural base plate)
    2_Cyan.stl           (cyan color layer)
    3_Magenta.stl        (magenta color layer)
    4_Yellow.stl         (yellow color layer)
    5_Top_White.stl      (lithotop / luminance-driven top white layer)

The numeric prefixes force the load order in most slicers; the colour
names make the files self-documenting.

Typical use
-----------
    # CLI
    python lithophane_cmy.py photo.jpg --width 100 --height 100 --px-per-mm 10
    python lithophane_cmy.py --demo

    # Programmatic
    from lithophane_cmy import process_image
    result = process_image("photo.jpg")
    C, M, Y = result.channels
    result.base_stl              # base plate
    result.cyan_layer_stl        # cyan color layer
    result.magenta_layer_stl     # magenta color layer
    result.yellow_layer_stl      # yellow color layer
    result.lithotop_stl          # white luminance layer
"""

